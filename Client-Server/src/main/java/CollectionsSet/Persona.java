
package CollectionsSet;


public class Persona {
    private long cedula;
    private String nombre;

    public Persona() {
        this.cedula = 0;
        this.nombre = "";
    }

    public long getCedula() {
        return cedula;
    }

    public void setCedula(long cedula) {
        this.cedula = cedula;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
}
